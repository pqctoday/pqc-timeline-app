def main():
    # Read source CSV
    with open(SRC, 'r', encoding='utf-8', newline='') as f:
        reader = csv.reader(f)
        rows = list(reader)

    header = rows[0]
    data = rows[1:]

    # Build set of existing software_name values (for deduplication)
    existing_names = {r[C['software_name']].strip() for r in data}

    # Apply in-place fixes
    fixes_applied = 0
    for r in data:
        name = r[C['software_name']].strip()
        if name in FIXES:
            for col_idx, transform in FIXES[name]:
                old = r[col_idx]
                r[col_idx] = transform(old)
                if r[col_idx] != old:
                    fixes_applied += 1

    # Filter out duplicate new rows
    deduped_new = []
    skipped = []
    for r in NEW_ROWS:
        name = r[C['software_name']].strip()
        if name in existing_names:
            skipped.append(name)
        else:
            deduped_new.append(r)
            existing_names.add(name)

    # Write output
    with open(DST, 'w', encoding='utf-8', newline='') as f:
        writer = csv.writer(f, lineterminator='\n')
        writer.writerow(header)
        writer.writerows(data)
        writer.writerows(deduped_new)

    total = len(data) + len(deduped_new)
    print(f'Done.')
    print(f'  Source rows  : {len(data)}')
    print(f'  Fixes applied: {fixes_applied}')
    print(f'  New rows added: {len(deduped_new)}')
    print(f'  Skipped (dup) : {len(skipped)}')
    if skipped:
        print(f'  Skipped names : {skipped}')
    print(f'  Total rows   : {total}')
    print(f'  Output       : {DST}')


if __name__ == '__main__':
    main()
